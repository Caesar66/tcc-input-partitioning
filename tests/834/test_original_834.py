import pytest
import importlib

test_cases = [
	([[3, 8, 0], [1, 4, 3], [6, 6, 6]], 4.11),
	([[7, 2, 5, 2, 7], [7, 5, 3, 5, 5], [9, 1, 2, 4, 1], [9, 3, 5, 5, 5], [6, 5, 4, 8, 6]], 4.84),
	([[9, 7, 9, 9, 9], [9, 3, 3, 2, 8], [1, 3, 4, 4, 3]], 5.53),
	([[7, 4, 8, 3, 7], [1, 2, 3, 5, 8], [8, 6, 1, 8, 1], [9, 9, 9, 5, 8], [3, 8, 5, 0, 5]], 5.32),
	([[0, 6, 1], [0, 9, 7], [9, 9, 2]], 4.78),
	([[5, 7, 9], [3, 0, 6], [1, 3, 8], [4, 7, 4], [6, 6, 8]], 5.13),
	([[5, 0, 5, 0], [0, 9, 5, 0], [6, 1, 5, 2], [0, 6, 9, 4], [1, 0, 5, 1]], 3.2),
	([[2, 2, 4], [5, 7, 8], [5, 9, 4], [0, 0, 4]], 4.17),
	([[5, 4, 1, 5], [4, 4, 3, 9], [2, 8, 7, 6], [5, 8, 9, 4], [2, 5, 2, 7]], 5.0),
	([[7, 1, 2], [8, 2, 4], [2, 4, 6], [5, 1, 3]], 3.75)
]

@pytest.mark.parametrize("matriz, output", test_cases)

def test_media_matriz(matriz, output, solution):
	imp = importlib.import_module(solution)
	assert imp.media_matriz(matriz) == output

