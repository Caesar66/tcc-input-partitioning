import pytest
import importlib

test_cases = [
    (0, 1),
    (1, 1),
    (100, 93326215443944152681699238856266700490715968264381621468592963895217599993229915608941463976156518286253697920827223758251185210916864000000000000000000000000),
    (97, 96192759682482119853328425949563698712343813919172976158104477319333745612481875498805879175589072651261284189679678167647067832320000000000000000000000)
]

@pytest.mark.parametrize("numero, output", test_cases)
def test_fatorial(numero, output, solution):
	imp = importlib.import_module(solution)
	assert imp.fatorial(numero) == output
